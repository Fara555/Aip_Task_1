#Найти сумму диагоналя(второстепенного)
# Input:
# 3 2 0
# 4 5 6
# 7 3 9
# Output: 12

def sum_secondary_diagonal(matrix):
    n = len(matrix)
    diagonal_sum = 0
    for i in range(n):
        diagonal_sum += matrix[i][n - i - 1]
    return diagonal_sum

# Входные данные
matrix = [
    [3, 2, 0],
    [4, 5, 6],
    [7, 3, 9]
]

# Вычисление суммы второстепенной диагонали
result = sum_secondary_diagonal(matrix)

# Вывод результата
print("Сумма второстепенной диагонали:", result)


#Напишите программу, которая определяет, сколько раз встречается заданное число x в данном массиве.
# Входные данные
# В первой строке задается одно натуральное число N, не превосходящее 1000 – размер массива.
# Во второй строке вводятся N чисел – элементы массива (целые числа, не превосходящие по модулю 1000).
# В третьей строке содержится одно целое число x , не превосходящее по модулю 1000.
# Выходные данные
# Вывести одно число – сколько раз встречается x в данном массиве.
# Примеры
# входные данные
# 5
# 1 2 3 4 5
# 3
# выходные данные
# 1

def count_occurrences(array, x):
    count = 0
    for num in array:
        if num == x:
            count += 1
    return count

# Ввод данных
N = int(input("Введите размер массива: "))
array = list(map(int, input("Введите элементы массива: ").split()))
x = int(input("Введите число x: "))

# Вычисление количества вхождений x в массив
occurrences = count_occurrences(array, x)

# Вывод результата
print("Количество вхождений числа x:", occurrences)
#В данном примере программа считывает размер массива, элементы массива и число x с помощью функции input(). Затем вызывается функция count_occurrences(), которая подсчитывает количество вхождений числа x в массив. Результат выводится на экран с помощью функции print().


#Спортсмен Василий участвовал в соревнованиях по хоккейболу и получил в личном зачете серебряную медаль. Известно, что участники, получившие одинаковое количество очков, награждаются одинаковыми наградами. Известно, что были разыграны золотые серебряные и бронзовые медали. В задаче не спрашиваются правила хоккейбола. Необходимо только определить сколько очков набрал Василий.Входные данные
# На первой строке дано число N (2 ≤ N ≤ 1000) количество спортсменов, участвовавших в соревнованиях, на второй N целых чисел – результаты через пробел.
# Выходные данные
# Требуется вывести одно число – результат Василия
# Примеры
# входные данные
# 5
# 4 3 3 1 2
# выходные данные
# 3
# входные данные
# 8
# 1 2 5 3 5 6 6 5
# выходные данные
# 5
def find_vasily_score(scores):
    scores.sort(reverse=True)  # Сортируем результаты по убыванию
    vasily_score = scores[1]  # Второй результат является результатом Василия
    return vasily_score

# Ввод данных
N = int(input("Введите количество спортсменов: "))
scores = list(map(int, input("Введите результаты спортсменов: ").split()))

# Вычисление результата Василия
vasily_score = find_vasily_score(scores)

# Вывод результата
print("Результат Василия:", vasily_score)

#В данном примере программа считывает количество спортсменов N и их результаты с помощью функции input(). Затем вызывается функция find_vasily_score(), которая сортирует результаты по убыванию и возвращает второй результат, соответствующий результату Василия. Результат Василия выводится на экран с помощью функции print().






